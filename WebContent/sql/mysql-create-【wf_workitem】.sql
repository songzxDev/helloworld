CREATE TABLE `wf_workitem` (
  `WF_WORKITEM_ID` varchar(40) NOT NULL,
  `PROCESS_INSTANCE_ID` varchar(40) NOT NULL,
  `PROCESS_NAME` varchar(300) NOT NULL,
  `WF_STEP_ID` varchar(40) NOT NULL,
  `WF_STEP_NAME` varchar(100) NOT NULL,
  `WF_STEP_TYPE` char(1) NOT NULL,
  `SENDER_NAME` varchar(100) NOT NULL,
  `SENDER_PARTYID` varchar(100) NOT NULL,
  `SENDER_CODE` varchar(300) NOT NULL,
  `SENDER_COMPLETED_DATETIME` timestamp NULL DEFAULT NULL,
  `PERFORMER_NAME` varchar(100) DEFAULT NULL,
  `PERFORMER_PARTYID` varchar(100) DEFAULT NULL,
  `PERFORMER_CODE` varchar(300) DEFAULT NULL,
  `PERFORMER_COMPLETED_DATETIME` timestamp NULL DEFAULT NULL,
  `READ_STATUS` char(1) DEFAULT '0',
  `DONE_STATUS` char(1) DEFAULT '0',
  `AUTHORIZE_STATUS` char(1) DEFAULT '0',
  `USABLE_STATUS` char(1) NOT NULL DEFAULT '1',
  `CREATE_DATETIME` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `MODIFY_DATETIME` timestamp NULL DEFAULT NULL,
  `WF_ENGINE_TYPE` varchar(40) DEFAULT NULL,
  `RESERVED_ATTRIBUTE_1` varchar(500) DEFAULT NULL,
  `RESERVED_ATTRIBUTE_2` varchar(500) DEFAULT NULL,
  `RESERVED_ATTRIBUTE_3` varchar(500) DEFAULT NULL,
  `RESERVED_ATTRIBUTE_4` varchar(500) DEFAULT NULL,
  `RESERVED_ATTRIBUTE_5` varchar(500) DEFAULT NULL,
  PRIMARY KEY (`WF_WORKITEM_ID`),
  KEY `WF_WORKITEM_INDEX_1` (`PROCESS_INSTANCE_ID`,`SENDER_NAME`,`PERFORMER_NAME`,`CREATE_DATETIME`,`SENDER_PARTYID`,`PERFORMER_PARTYID`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;